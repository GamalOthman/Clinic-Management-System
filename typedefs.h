#ifndef _typedefs_
#define _typedefs_

// one byte size:
typedef unsigned char u8;
typedef signed char s8;

// two byte size:
typedef unsigned short u16;
typedef signed short s16;

// four byte size:
typedef unsigned int u32;
typedef signed int s32;

// eight byte size:
typedef unsigned long u64;
typedef signed long s64;

// float:
typedef float f32;
typedef double f64;

#endif
